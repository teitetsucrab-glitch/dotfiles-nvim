-- 1. カラーパレットの定義 (chalkboard_eye_friendly.lua から移植)
-- base00 - base07: 背景・モノトーン系
-- base08 - base0F: アクセントカラー (赤、橙、黄、緑、青、紫、茶、マゼンタ)
local chalkboard_palette = {
    base00 = '#3C503C', -- 背景 (黒板の緑)
    base01 = '#455945', -- 背景より少し明るい (カーソル行など)
    base02 = '#4C644C', -- 選択範囲・折り畳み
    base03 = '#606060', -- コメント・非表示テキスト (暗いチョーク)
    base04 = '#767676', -- 暗い文字
    base05 = '#F0F0D2', -- 標準文字 (チョークの白)
    base06 = '#F0F0D2', -- 明るい文字 (標準と同じに設定)
    base07 = '#FFFFFF', -- 真っ白 (強調、エラーなど)
    
    base08 = '#D78C96', -- 赤 (パステル赤) -> 変数、削除など
    base09 = '#F0DC96', -- 橙 (パステル黄で代用) -> 定数、数値
    base0A = '#F0DC96', -- 黄 (パステル黄) -> クラス、検索
    base0B = '#A0D28C', -- 緑 (パステル緑) -> 文字列
    base0C = '#78AAEE', -- シアン (パステル青で代用) -> 正規表現など
    base0D = '#78AAEE', -- 青 (パステル青) -> 関数、メソッド
    base0E = '#BE8CE2', -- 紫 (パステル紫) -> キーワード
    base0F = '#D78C96', -- 茶 (パステル赤で代用) -> 特殊
}

-- 2. mini.base16 のセットアップ
require('mini.base16').setup({
	palette = chalkboard_palette,
	use_cterm = true,
	plugins = {
		default = true,  -- vim標準のハイライト
		['nvim-mini/mini.nvim'] = true, -- mini.nvimへの適用
		-- 必要に応じて他のプラグインもtrueにする
	}
})


